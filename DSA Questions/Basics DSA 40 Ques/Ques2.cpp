

// 2. WAP to take input 1 number from the user and print its square and cube.

#include <iostream>
using namespace std;

int main() {
    int num;
    cout << "Enter a number: ";
    cin >> num;
    cout << "Square of " << num << " is " << (num * num) << endl;
    cout << "Cube of " << num << " is " << (num * num * num) << endl;
    return 0;
}



