// 1. WAP to take input 2 numbers from the user and print the sum of the numbers.

#include <iostream>
using namespace std;
int main() {
    int num1, num2;
    cout << "Enter two numbers: ";
    cin >> num1 >> num2;
    cout << "The sum of " << num1 << " and " << num2 << " is " << (num1 + num2) << endl;
    return 0;
}
